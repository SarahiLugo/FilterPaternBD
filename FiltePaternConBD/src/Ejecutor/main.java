    public void llenarTabla(){
        jTable1.setModel(modelo);
        modelo.setColumnIdentifiers(new String[]{"ID","nombre","descripcion","stock","proveedor"});
        ProductoCrtl ctrlP=new ProductoCrtl();
        ArrayList<BD> lista=(ArrayList<BD>) (ctrlP.consultar());
        
        for (int i = 0; i < lista.size(); i++) {
            modelo.addRow(new Object[]{
            lista.get(i).getId(),
            lista.get(i).getNombre(),
            lista.get(i).getDescripcion(),
            lista.get(i).getStock(),
            lista.get(i).getProveedor()});
        }
    }
    
    public void limpiarTabla(){
        int fila=jTable1.getRowCount();
        for (int i = 0; i < fila; i++) {
            modelo.removeRow(0);
        }
    }
