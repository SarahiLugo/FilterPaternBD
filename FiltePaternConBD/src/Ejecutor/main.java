    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int fila = jTable1.getSelectedRow();
        BD Prod = new BD();
        Prod.setId(Integer.parseInt(jTable1.getValueAt(fila, 0).toString()));
        ProductoCrtl ctrlPro=new ProductoCrtl();
        int resp=JOptionPane.showConfirmDialog(null, "¿Estas seguro?","alerta",JOptionPane.YES_NO_OPTION);
        if (resp==0) {
            if (ctrlPro.Eliminar(Prod)) {
                limpiarTabla();
                llenarTabla();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

